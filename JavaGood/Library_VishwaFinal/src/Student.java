
public class Student {
	
	private int roll;
	private String clg;
	private String classType;
	private String cardNumber;
	private int issuedBooks_iiitd;
	private int issuedBooks_dtu;
	
	
	
	public int getIssuedBooks_iiitd() {
		return issuedBooks_iiitd;
	}
	public void setIssuedBooks_iiitd(int issuedBooks_iiitd) {
		this.issuedBooks_iiitd = issuedBooks_iiitd;
	}
	public int getIssuedBooks_dtu() {
		return issuedBooks_dtu;
	}
	public void setIssuedBooks_dtu(int issuedBooks_dtu) {
		this.issuedBooks_dtu = issuedBooks_dtu;
	}
	public int getRoll() {
		return roll;
	}
	public void setRoll(int roll) {
		this.roll = roll;
	}
	public String getClg() {
		return clg;
	}
	public void setClg(String clg) {
		this.clg = clg;
	}
	public String getClassType() {
		return classType;
	}
	public void setClassType(String classType) {
		this.classType = classType;
	}
	public String getCardNumber() {
		return cardNumber;
	}
	public void setCardNumber(String cardNumber) {
		this.cardNumber = cardNumber;
	}
	
	

}
